package com.example.dmucatch_store.store.account;

public class Userinfo {
    public static String storeSeq;
    public static String id;
    public static String name;
    public static String pwd;
    public static String phone;
    public static String address;
    public static String class_code;

    public static String getStoreSeq() {
        return storeSeq;
    }

    public static void setStoreSeq(String storeSeq) {
        Userinfo.storeSeq = storeSeq;
    }

    public static String getID() {
        return id;
    }

    public static void setID(String ID) {
        Userinfo.id = ID;
    }

    public static String getName() {
        return name;
    }

    public static void setName(String name) {
        Userinfo.name = name;
    }

    public static String getPwd() {
        return pwd;
    }

    public static void setPwd(String pwd) {
        Userinfo.pwd = pwd;
    }

    public static String getPhone() {
        return phone;
    }

    public static void setPhone(String phone) {
        Userinfo.phone = phone;
    }

    public static String getAddress() {
        return address;
    }

    public static void setAddress(String address) {
        Userinfo.address = address;
    }

    public static String getClass_code() {
        return class_code;
    }

    public static void setClass_code(String class_code) {
        Userinfo.class_code = class_code;
    }

}
